using System;
using System.Collections.Generic;
using System.Text;

namespace Animal_Clinic
{
    class Animal
    {
        public static string name;  // the name field
        public string Name    // the Name property
        {
            get
            {
                return name;
            }
            set
            {
                name = value;
            }
        }

        public static string breed;  // the name field
        public string Breed    // the Name property
        {
            get
            {
                return breed;
            }
            set
            {
                breed = value;
            }
        }

        
    
    }
}
